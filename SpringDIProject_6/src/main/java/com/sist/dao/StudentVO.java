package com.sist.dao;

public class StudentVO {
	private int hakbum,eng,math,kor;
	private String name;
	public int getHakbum() {
		return hakbum;
	}
	public void setHakbum(int hakbum) {
		this.hakbum = hakbum;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
